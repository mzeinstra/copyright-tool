          <h2>Individuele en collectieve licenties</h2>

          <p>Bij <strong>individuele</strong> licenties zijn twee partijen betrokken die alleen zichzelf vertegenwoordigen. Er worden afspraken gemaakt tussen de licentiegever en de licentienemer over de exploitatie van één of meerdere auteursrechtelijke werken. Dit kan bijvoorbeeld ook worden opgenomen in de schenkingsovereenkomst.</p>

