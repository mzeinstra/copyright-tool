          <h2>Open licenties</h2>

          <p>Met <strong>Open licenties</strong>, waaronder Creative Commons-licenties, geeft de licentiegever een grote groep, soms niet nader beschreven, licentienemers toestemming voor het gebruik van auteursrechtelijk beschermde werken.</p>

          <p>Creative Commons-licenties bieden rechthebbenden de vrijheid om op een flexibele manier met hun auteursrechten om te gaan. Het aanbieden van je werk onder een Creative Commons-licentie betekent niet dat je je auteursrechten opgeeft. Het betekent dat je sommige van je rechten onder bepaalde voorwaarden aanbiedt aan wie er maar gebruik van wil maken. Als rechthebbende bepaal je dus zelf of en hoe je je werk beschermt en welke licentievorm je kiest.</p>

          <p><a href="https://creativecommons.nl/faq/">Lees hier meer over Creative Commons</a></p>

          <h2>Communiceer de licentievorm en toestemming</h2>

